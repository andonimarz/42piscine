#include <stdio.h>

int	ft_atoi(char *str);

int	main(void)
{
	char string[] = " ---++-++-+234ab567";

	printf("%d\n", ft_atoi(string));
	return (0);
}

int	ft_atoi(char *str)
{
	int	sign;
	int	i;
	int	num;

	sign = 1;
	i = 0;
	num = 0;
	while (str[i] == ' ' || str[i] == '\f' || str[i] == '\n'
		|| str[i] == '\r' || str[i] == '\t' || str[i] == '\v')
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -sign;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = (num * 10) + (str[i] - '0');
		i++;
	}
	num = num * sign;
	return (num);
}
